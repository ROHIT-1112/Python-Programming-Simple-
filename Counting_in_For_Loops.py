# Counting in Loop in Python
count = 0
cars = ['alto','kia','xcent','swift','i10','defender','fortuner','Xuv 500','Scorpio']
for car in cars :
    count = count + 1 # Counting Loop value .
    print('Current car :- {} , Number of times loop executed :- {} .'.format(car,count))
print("\nTotal times Loop Executed :- {}.".format(count))
print("Loop Finished !!!! Process Over !!!!")